    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        String username = this.usernameTxt.getText();
        String password = String.valueOf(this.passwordTxt.getPassword());
        
        String query = "";
        if (!isEditing){
            query = String.format(
                "INSERT INTO admins(username,password) VALUES ('%s','%s')",
                username, password
            );
        } else {
            query = String.format(
                "UPDATE programs SET username='%s', password='%s' WHERE id=%s",
                username, password , adminId
            );
        }
        
        
        if (db.query(query)){
            this.resetFields();
            JOptionPane.showMessageDialog(this, "Admin was saved successfully","Success!", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Error in adding a data to the database","Error!", JOptionPane.ERROR_MESSAGE);
        }
        
        
        
    }//GEN-LAST:event_saveBtnActionPerformed

