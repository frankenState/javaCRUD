    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        String firstName = this.firstNameTxt.getText();
        String lastName = this.lastNameTxt.getText();
        String middleName = this.middleNameTxt.getText();
        String gender = this.genderCombo.getSelectedItem().toString();
        String programId = this.programs.get(this.programCombo.getSelectedIndex())[0];
        
        String query = "";
        if (!isEditing){
            query = String.format(
                "INSERT INTO students(firstName,lastName,middleName,gender,programId) VALUES ('%s','%s','%s','%s','%s')",
                firstName, lastName, middleName, gender, programId
            );
        } else {
            query = String.format(
                "UPDATE students SET firstName='%s', lastName='%s', middleName='%s', gender='%s', programId='%s' WHERE id=%s",
                firstName, lastName, middleName, gender, programId, studentId
            );
        }
        
        
        if (db.query(query)){
            this.resetFields();
            JOptionPane.showMessageDialog(this, "Student was saved successfully","Success!", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Error in adding a data to the database","Error!", JOptionPane.ERROR_MESSAGE);
        }
        
        
        
    }//GEN-LAST:event_saveBtnActionPerformed

