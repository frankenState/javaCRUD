    private void adminTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_adminTableMouseClicked
        this.isEditing = true;
        this.newBtn.setEnabled(true);
        this.deleteBtn.setEnabled(true);
        int row = this.adminTable.getSelectedRow();
        
        this.adminId = this.adminTable.getValueAt(row, 0).toString();
        String username = this.adminTable.getValueAt(row, 1).toString();
        String password = this.adminTable.getValueAt(row, 2).toString();
        
        this.passwordTxt.setText(password);
        this.usernameTxt.setText(username);
        
    }//GEN-LAST:event_adminTableMouseClicked

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int sure = JOptionPane.showConfirmDialog(this, "Are you sure?", "Delete confirmation.", JOptionPane.WARNING_MESSAGE);
        
        adminId = adminTable.getValueAt(adminTable.getSelectedRow(), 0).toString();
        
        if (sure == 0){
            boolean success = db.query(String.format("DELETE FROM admins WHERE id=%s", adminId));
            if (success){
                this.resetFields();
                JOptionPane.showMessageDialog(this, "Admin was deleted successfully","Success!", JOptionPane.INFORMATION_MESSAGE);
            } else 
                JOptionPane.showMessageDialog(this, "Deletion Error.","Error!", JOptionPane.ERROR_MESSAGE);
        
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

