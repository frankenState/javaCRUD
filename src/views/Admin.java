    public void loadTable(boolean searching){
        String query ="SELECT * FROM admins";
        
        if (searching){
            String txt = searchTxt.getText();
            query += " WHERE admins.username LIKE '%" + txt + "%'";
        } 

        ArrayList<String[]> data = db.select(query, new String[]{"id","username","password"});
        
        DefaultTableModel model = (DefaultTableModel)this.adminTable.getModel();
        
        model.setRowCount(0);
        
        data.forEach((row) -> {
            String hash = "", ps = row[2];
            
            for (int i = 0; i < ps.length(); i++) 
                hash += "*";
            
            row[2] = hash;
            model.addRow(row);
        });
        

    }
    
