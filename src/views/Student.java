    public void initDB(){
        this.db = new DB();
        this.loadTable(false);
        
        // we need to load the programs
        ArrayList<String[]> programs = db.select("SELECT * FROM programs", new String[]{"id","initials"});
      // setModel(new javax.swing.DefaultComboBoxModel<>(new String[] { "MALE", "FEMALE" }));
        DefaultComboBoxModel model = (DefaultComboBoxModel)this.programCombo.getModel();
        programs.forEach((row) -> {
            model.addElement(row[1]);
        });
        this.programCombo.setModel(model);
        this.programs = programs;
        
        this.deleteBtn.setEnabled(false);
        this.newBtn.setEnabled(false);
    }
    
    
    public void loadTable(boolean searching){
        String query ="SELECT students.*, programs.initials FROM students LEFT JOIN programs ON students.programId=programs.id";;
        
        if (searching){
            String txt = searchTxt.getText();
            query += " WHERE students.firstName LIKE '%" + txt + 
                    "%' OR students.lastName LIKE '%"+ txt + 
                    "%' OR students.middleName LIKE '%"+ txt +
                    "%' OR programs.initials LIKE '%"+ txt + 
                    "%' OR students.gender LIKE '%" + txt + "%'";
        } 

        ArrayList<String[]> data = db.select(query, new String[]{"id","firstName","lastName","middleName","gender","initials"});
        
        DefaultTableModel model = (DefaultTableModel)this.studentTable.getModel();
        
        model.setRowCount(0);
        
        data.forEach((row) -> {
            model.addRow(row);
        });
        

    }
    
    public void resetFields(){
        this.newBtn.setEnabled(false);
        this.deleteBtn.setEnabled(false);
        this.loadTable(false);
        this.firstNameTxt.setText("");
        this.lastNameTxt.setText("");
        this.middleNameTxt.setText("");
        this.genderCombo.setSelectedIndex(0);
        this.programCombo.setSelectedIndex(0);
        this.isEditing = false;
        this.studentId = "";

    }

