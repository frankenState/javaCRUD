    private void studentTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_studentTableMouseClicked
        this.isEditing = true;
        this.newBtn.setEnabled(true);
        this.deleteBtn.setEnabled(true);
        int row = this.studentTable.getSelectedRow();
        
        this.studentId = this.studentTable.getValueAt(row, 0).toString();
        String firstName = this.studentTable.getValueAt(row, 1).toString();
        String lastName = this.studentTable.getValueAt(row, 2).toString();
        String middleName = this.studentTable.getValueAt(row, 3).toString();
        String gender = this.studentTable.getValueAt(row, 4).toString();
        String program = this.studentTable.getValueAt(row, 5).toString();
        
        this.firstNameTxt.setText(firstName);
        this.lastNameTxt.setText(lastName);
        this.middleNameTxt.setText(middleName);
        this.genderCombo.setSelectedItem(gender);
        this.programCombo.setSelectedItem(program);
    }//GEN-LAST:event_studentTableMouseClicked

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int sure = JOptionPane.showConfirmDialog(this, "Are you sure?", "Delete confirmation.", JOptionPane.WARNING_MESSAGE);
        
        studentId = studentTable.getValueAt(studentTable.getSelectedRow(), 0).toString();
        
        if (sure == 0){
            boolean success = db.query(String.format("DELETE FROM students WHERE id=%s", studentId));
            if (success){
                this.resetFields();
                JOptionPane.showMessageDialog(this, "Student was deleted successfully","Success!", JOptionPane.INFORMATION_MESSAGE);
            } else 
                JOptionPane.showMessageDialog(this, "Deletion Error.","Error!", JOptionPane.ERROR_MESSAGE);
        
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

